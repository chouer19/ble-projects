#ifndef SPIDER_BLE_PROTOCOL_H
#define SPIDER_BLE_PROTOCOL_H

#include <zephyr/bluetooth/uuid.h>

/* Service: A0010001-0000-1000-8000-00805F9B34FB */
#define SPIDER_BLE_SVC_UUID_VAL \
	BT_UUID_128_ENCODE(0xA0010001, 0x0000, 0x1000, 0x8000, 0x00805F9B34FB)

/* Characteristic cmd: A0010002-0000-1000-8000-00805F9B34FB */
#define SPIDER_BLE_CHR_CMD_UUID_VAL \
	BT_UUID_128_ENCODE(0xA0010002, 0x0000, 0x1000, 0x8000, 0x00805F9B34FB)

#define SPIDER_BLE_SVC_UUID    BT_UUID_DECLARE_128(SPIDER_BLE_SVC_UUID_VAL)
#define SPIDER_BLE_CHR_CMD_UUID BT_UUID_DECLARE_128(SPIDER_BLE_CHR_CMD_UUID_VAL)

#define SPIDER_BLE_OP_MOTION            0x01U
#define SPIDER_BLE_OP_STOP              0x02U
#define SPIDER_BLE_OP_SET_MOTION_DELAY  0x10U
#define SPIDER_BLE_OP_SET_SERVO_SPEED   0x11U

#define SPIDER_BLE_LEG_NONE  0xFFU

#define SPIDER_BLE_MOTION_DELAY_MIN_MS  100U
#define SPIDER_BLE_MOTION_DELAY_MAX_MS  2000U
#define SPIDER_BLE_SERVO_SPEED_MAX_DEG_S 600U

#endif /* SPIDER_BLE_PROTOCOL_H */
