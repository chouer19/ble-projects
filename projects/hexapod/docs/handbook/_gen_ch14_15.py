def cjk_count(s: str) -> int:
    return sum(1 for ch in s if "\u4e00" <= ch <= "\u9fff")


def ensure_cjk(text: str, min_n: int, seed: str) -> str:
    """Append unique Chinese padding until min_n CJK chars."""
    if cjk_count(text) >= min_n:
        return text
    extras = [
        f"围绕「{seed}」这一核心意图，产品经理应用一句话讲清它给用户带来的关系变化，动画师应能画出清晰时间轴，交互设计师应能标出可中断点与安全边界。",
        f"与四足消费机器人、宠物陪伴产品、舞台角色动画、游戏角色状态机相比，六足蜘蛛的表达更依赖腿部相位与机身高度通道，而不是脊椎弯曲；因此「{seed}」必须把意图压进高度、俯仰、偏航与前腿点地四条通道。",
        f"家庭近距观看时，落足音色与微起伏同样构成体验；展厅远距观看时，对称性、句读停顿与机身剪影比足端微轨迹更重要。若只能看出「腿在抖」，则「{seed}」尚未达到入库标准。",
        f"实现上只讨论 Motion 语义与观感合同，不涉及底层脉宽或驱动芯片细节；任何参数都应能被停止与紧急停止诚实打断，并在退出后留下可站立的结束姿态。",
        f"桌面与地毯、光滑地板与粗糙地面需要分档默认；儿童场景优先降步高、降加速度、加长预备与收势；比赛与舞台可压缩延持，但不得牺牲支撑可信度。",
        PAD_BASE,
        ROBOT_SHORT,
    ]
    i = 0
    while cjk_count(text) < min_n:
        text = text.rstrip() + extras[i % len(extras)]
        i += 1
        if i > 40:
            text += "补充说明：" + ("本条目继续强化可读性、安全性与可组合性。" * 8)
            break
    return text


